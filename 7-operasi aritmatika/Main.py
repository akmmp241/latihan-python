# operasi aritmatika

a = 10 
b = 2

# operasi pertambahan (+)
hasil = a + b
print(hasil)

# # operasi pengurangan (-)
hasil = a - b
print(hasil)

# operasi perkalian (*)
hasil = a * b
print(hasil)

# operasi pembagian (/)
hasil = a / b
hasil1 = a / 3
print(hasil,hasil1)

# operasi eksponen (pangkat (**))
hasil = a ** b
hasil1 = a ** 3
print(hasil,hasil1)

# operasi modulus (%)
hasil = a % b
hasil1 = a % 3
print(hasil)

# operasi floor division (//)
hasil = a // b
hasil1 = a // 3
print(hasil,hasil1)
