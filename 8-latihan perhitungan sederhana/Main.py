# latihan onversi satuan temperature

# program konversi celcius ke satuan lain

print("\nProgram konversi celcius ke satuan lain")

celcius = int(input("Masukan satuan celcius: "))

# menkonversi ke reamur
reamur = (4/5) * celcius
print("Besar dalam satuan remur adalah: ", reamur)

# mengkonversi ke fahrenheit
fahrenheit = ((9/5) * celcius) + 32
print("Besar dalam satuan fahrenheit adalah: ", fahrenheit)

# mengkonversi ke kelvin
kelvin = celcius + 273
print("Besar dalam satuan kelvin adalah: ", kelvin)